    <li>Autoimune bolesti kože (npr. lupus, skleroderma).</li>
    <li>Rosacea i teški oblik akni (upalne) - RF može pogoršati stanje kod aktivne upale.</li>
    <li>Epilepsija.</li>
    <li>Nedavni dermalni fileri ili Botox - obično se preporuča pričekati 2–4 tjedna.</li>
  </ul>
